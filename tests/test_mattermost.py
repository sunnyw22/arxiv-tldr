class TestUploadFile:
    @patch("src.reports.mattermost.requests.post")
    def test_successful_upload(self, mock_post, tmp_path):
        mock_resp = MagicMock()
        mock_resp.ok = True
        mock_resp.json.return_value = {"file_infos": [{"id": "file123"}]}
        mock_post.return_value = mock_resp

        f = tmp_path / "digest.md"
        f.write_text("# Digest")

        result = upload_file(
            "https://mm.example.com", "tok", "ch1", str(f)
        )

        assert result == "file123"
        call_url = mock_post.call_args[0][0]
        assert call_url == "https://mm.example.com/api/v4/files"

    @patch("src.reports.mattermost.requests.post")
    def test_http_error_returns_none(self, mock_post, tmp_path):
        mock_resp = MagicMock()
        mock_resp.raise_for_status.side_effect = requests.HTTPError("403 Forbidden")
        mock_post.return_value = mock_resp

        f = tmp_path / "digest.md"
        f.write_text("# Digest")

        result = upload_file(
            "https://mm.example.com", "tok", "ch1", str(f)
        )
        assert result is None

    @patch("src.reports.mattermost.requests.post")
    def test_connection_error_returns_none(self, mock_post, tmp_path):
        mock_post.side_effect = requests.ConnectionError("refused")

        f = tmp_path / "digest.md"
        f.write_text("# Digest")

        result = upload_file(
            "https://mm.example.com", "tok", "ch1", str(f)
        )
        assert result is None

    @patch("src.reports.mattermost.requests.post")
    def test_strips_trailing_slash(self, mock_post, tmp_path):
        mock_resp = MagicMock()
        mock_resp.ok = True
        mock_resp.json.return_value = {"file_infos": [{"id": "file1"}]}
        mock_post.return_value = mock_resp

        f = tmp_path / "digest.md"
        f.write_text("# Digest")

        upload_file("https://mm.example.com/", "tok", "ch1", str(f))

        call_url = mock_post.call_args[0][0]
        assert "//api" not in call_url
        assert call_url == "https://mm.example.com/api/v4/files"


