class TestSendDigest:
    @patch("src.reports.mattermost.create_post")
    @patch("src.reports.mattermost.upload_file")
    def test_upload_and_post_success(self, mock_upload, mock_create, tmp_path):
        mock_upload.return_value = "file123"
        mock_create.return_value = True

        f = tmp_path / "digest.md"
        f.write_text("# Report")

        result = send_digest(
            "https://mm.example.com", "tok", "ch1", str(f), "summary text",
        )

        assert result is True
        mock_upload.assert_called_once()
        mock_create.assert_called_once()
        _, kwargs = mock_create.call_args
        assert kwargs.get("file_ids") == ["file123"]

    @patch("src.reports.mattermost.create_post")
    @patch("src.reports.mattermost.upload_file")
    def test_upload_fails_posts_summary_only(self, mock_upload, mock_create, tmp_path):
        mock_upload.return_value = None
        mock_create.return_value = True

        f = tmp_path / "digest.md"
        f.write_text("# Report")

        result = send_digest(
            "https://mm.example.com", "tok", "ch1", str(f), "summary text",
        )

        assert result is True
        mock_create.assert_called_once()
        _, kwargs = mock_create.call_args
        assert "file_ids" not in kwargs

    @patch("src.reports.mattermost.create_post")
    @patch("src.reports.mattermost.upload_file")
    def test_post_fails_returns_false(self, mock_upload, mock_create, tmp_path):
        mock_upload.return_value = "file123"
        mock_create.return_value = False

        f = tmp_path / "digest.md"
        f.write_text("# Report")

        result = send_digest(
            "https://mm.example.com", "tok", "ch1", str(f), "summary text",
        )

        assert result is False

    @patch("src.reports.mattermost.create_post")
    @patch("src.reports.mattermost.upload_file")
    def test_nonexistent_file_posts_summary_only(
        self, mock_upload, mock_create
    ):
        mock_create.return_value = True

        result = send_digest(
            "https://mm.example.com",
            "tok",
            "ch1",
            "/tmp/nonexistent_file_12345.md",
            "summary text",
        )

        assert result is True
        mock_upload.assert_not_called()
        mock_create.assert_called_once()


